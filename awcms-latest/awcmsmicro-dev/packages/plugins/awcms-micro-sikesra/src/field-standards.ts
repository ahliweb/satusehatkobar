export const SIKESRA_FIELD_STANDARDS = SIKESRA_DATA_MODULES.flatMap((module) => [
	...coreFields(module),
	...MODULE_SPECIFIC_FIELDS[module],
]) satisfies SikesraFieldStandardDto[];

export const SIKESRA_MODULE_FIELD_VALIDATION_SCHEMAS = SIKESRA_DATA_MODULES.map((module) => {
	const standards = SIKESRA_FIELD_STANDARDS.filter((standard) => standard.module === module);
	const restrictedExportFields = standards
		.filter(
			(standard) =>
				standard.dataClass === "sensitive_personal" ||
				standard.dataClass === "restricted" ||
				standard.validationRules.includes("restricted_export_reason"),
		)
		.map((standard) => standard.key);
	return {
		module,
		requiredFields: standards
			.filter((standard) => standard.required)
			.map((standard) => standard.key),
		importableFields: standards
			.filter((standard) => standard.importable)
			.map((standard) => standard.key),
		exportableFields: standards
			.filter((standard) => standard.exportable)
			.map((standard) => standard.key),
		publicSafeFields: standards
			.filter((standard) => standard.publicSafe)
			.map((standard) => standard.key),
		ktpAddressFields: standards
			.filter((standard) => standard.key.startsWith("alamat_ktp_"))
			.map((standard) => standard.key),
		domicileAddressFields: standards
			.filter((standard) => standard.key.startsWith("alamat_domisili_"))
			.map((standard) => standard.key),
		restrictedExportFields,
	};
}) satisfies SikesraModuleFieldValidationSchema[];

export function getSikesraFieldStandardsForModule(
	module: SikesraDataModule,
): SikesraFieldStandardDto[] {
	return SIKESRA_FIELD_STANDARDS.filter((standard) => standard.module === module);
}

export function getSikesraFieldValidationSchema(
	module: SikesraDataModule,
): SikesraModuleFieldValidationSchema {
	const schema = SIKESRA_MODULE_FIELD_VALIDATION_SCHEMAS.find((item) => item.module === module);
	if (!schema) throw new Error(`Unknown SIKESRA module: ${module}`);
	return schema;
}

function title(value: string): string {
	return value
		.split("_")
		.map((part) => `${part.charAt(0).toUpperCase()}${part.slice(1)}`)
		.join(" ");
}

function addressLabel(key: string): string {
	return title(key.replace("alamat_", "").replace("ktp", "KTP"));
}
