function personalAddressFields(module: SikesraDataModule): SikesraFieldStandardDto[] {
	const table = SIKESRA_D1_TABLES.personProfiles;
	return [...SIKESRA_KTP_ADDRESS_FIELD_KEYS, ...SIKESRA_DOMICILE_ADDRESS_FIELD_KEYS].map((key) =>
		field(module, {
			key,
			label: addressLabel(key),
			fieldGroup: "address",
			dataClass: key === "alamat_domisili_sama_dengan_ktp" ? "personal" : "sensitive_personal",
			required:
				SIKESRA_KTP_ADDRESS_FIELD_KEYS.includes(key as never) ||
				key === "alamat_domisili_sama_dengan_ktp",
			dataType: key === "alamat_domisili_sama_dengan_ktp" ? "boolean" : "string",
			maxLength: key.endsWith("_detail") ? 500 : 40,
			storageTable: table,
			importable: true,
			exportable: true,
			publicSafe: false,
			maskByDefault: true,
			validationRules:
				key === "alamat_domisili_sama_dengan_ktp"
					? ["boolean", "store_normalized_domicile_snapshot"]
					: ["personal_address", "restricted_export_reason"],
			notes: key.startsWith("alamat_ktp_")
				? "KTP address field; never public-safe."
				: "Domicile address field; never public-safe.",
		}),
	);
}

function detailField(
	module: SikesraDataModule,
	key: string,
	label: string,
	fieldGroup: SikesraFieldGroup,
	dataClass: SikesraFieldDataClass,
	required = false,
	dataType: SikesraFieldDataType = "string",
	validationRules: string[] = [],
): SikesraFieldStandardDto {
	return field(module, {
		key,
		label,
		fieldGroup,
		dataClass,
		required,
		dataType,
		storageTable: MODULE_DETAIL_TABLES[module],
		importable: true,
		exportable: dataClass !== "restricted",
		publicSafe: false,
		maskByDefault: dataClass !== "non_personal",
		validationRules: required ? ["required", ...validationRules] : validationRules,
	});
}

